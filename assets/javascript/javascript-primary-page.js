// Phần sort option
function SortOption() {
    var valueOption = document.querySelector('.home-filter__sort-option-label');
    var sortItems = document.querySelectorAll('.home-filter__sort-option-item');

    for(var item of sortItems) {
        item.addEventListener('click', ProductsSort);
    }

    function ProductsSort(event) {
        // Lấy ra số trang hiện tại
        var currentPageNum = document.querySelector('.home-filter__page-num-present').innerHTML;
        // Hiển thị lại label của thanh option
        valueOption.innerHTML = event.currentTarget.querySelector('span.home-filter__sort-option-link').innerHTML;
        // Xác định item là ascending hay descending để sort
        if(event.currentTarget.getAttribute('title') === 'ascending') {
            productsInfor.sort(function(a, b) {
                return parseInt(a.curentPrice.replaceAll('.', '')) - parseInt(b.curentPrice.replaceAll('.', ''));
            });
        } else {
            productsInfor.sort(function(a, b) {
                return parseInt(b.curentPrice.replaceAll('.', '')) - parseInt(a.curentPrice.replaceAll('.', ''));
            });
        }

        // Update lại info products sau khi đã sort lại
        ChargeInfoProductSort(currentPageNum);
    }
}

